void main() {
  //Introducing Variables In Dart:
  var myName = "Shubham";
  print("Variable => $myName");
  //Dynamic Variables In Dart:
  dynamic fullName = "Shubham Kadariya";
  print("FullName => $fullName");
  //Static Variables In Dart:
  String firstName = "John";
  print("FirstName => $firstName");
  //Constants In Dart:
  const String firstWord = "Hey";
  final String secondWord = "Jude!";
  print("$firstWord $secondWord");
}
